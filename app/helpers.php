if (! function_exists('str_sanitize')) {

    /**
     * Clean filename from some characters
     *
     * @param $string
     * @param bool $force_lowercase
     * @param bool $anal
     * @return bool|mixed|string
     */
    function str_sanitize($string, $force_lowercase = true, $anal = false)
    {
        $strip = array("~", "`", "!", "@", "#", "$", "%", "^", "&", "*", "(", ")", "_", "=", "+", "[", "{", "]",
            "}", "\\", "|", ";", ":", "\"", "'", "&#8216;", "&#8217;", "&#8220;", "&#8221;", "&#8211;", "&#8212;",
            "â€”", "â€“", ",", "<", ".", ">", "/", "?");
        $clean = trim(str_replace($strip, "", strip_tags($string)));
        $clean = preg_replace('/\s+/', "-", $clean);
        $clean = ($anal) ? preg_replace("/[^a-zA-Z0-9]/", "", $clean) : $clean ;

        return ($force_lowercase) ?
            (function_exists('mb_strtolower')) ?
                mb_strtolower($clean, 'UTF-8') :
                strtolower($clean) :
            $clean;
    }
}

if (! function_exists('slug_filename'))
{
    function slug_filename($filename)
    {
        return str_slug(str_sanitize(pathinfo($filename)['filename'])) . '.' . pathinfo($filename)['extension'];
    }
}
